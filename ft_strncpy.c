#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = 0;
	while (len-- && *src)
		dst[i++] = *src++;
	while (len-- + 1)
		dst[i++] = '\0';
	return (dst);
}
